# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal e considerado
# positivo.
# print(11) # int
# print(-11) # int
# print(0)

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante.
# float sem sinal e considerado positivo.
# print(1.1, 10.11)
# print(0.0, -1.5)

# A funcao type mostra o tipo que o Python
# inferiu ao valor.
print(type('Otavio'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))